print("Calculo del área y perímetro de un rectángulo ")

##print("Introduce la base del rectángulo")

base = int(input("Introduce la base del rectángulo\n"))

##print("Introduce la altura del rectángulo")

altura = int(input("Introdude la altura del rectángulo \n"))


area = base * altura

perimetro = 2 * (base + altura)

print("Área del rectangulo:" , area, "cm")
print("Perimetro del rectangulo:" , perimetro, "cm")


